namespace Emeraude.Pages;

/// <summary>
/// Represent single parameter defined by key and value.
/// </summary>
public class EmParameter
{
    /// <summary>
    /// Key of the parameter.
    /// </summary>
    public string Key { get; set; }

    /// <summary>
    /// Value of the parameter.
    /// </summary>
    public object Value { get; set; }
}